/* 在指定坐标的位置打印int行数字 */
/* 数字数组里的每个0/1代表一个像素,输出到屏幕时会将每个点扩大size倍 */

void printdig(int x, int y, int num) {
	int j, k, m, n, p, size = 2;
	p = x;
	if (num > 9)
		printdig(x - 11, y, num / 10); 
	num = num % 10;

		for (j = 0; j < 5; j++) {
			for (k = 0; k < 10; k++) {
				if (numgra[num][j][k]) {
					for (m = 0; m < size; m++)
						for (n = 0; n < size; n++)
							fb_draw_point(x+m, y+n, 0xFE);
				}
				x += size;
			}
			x = p; 
			y += size;
		}	
}
