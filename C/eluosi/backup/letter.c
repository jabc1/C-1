void printlet(int x, int y, char *word) {
	int j, k, m, n, p, p2, p3, lett, size = 2;
	p3 = p = x;
	p2 = y;
	
	while ((lett = *word++) != '\0')	{
		if (lett == '\n') {
			p2 += size * 7;
			y = p2;
			x = p3;
			continue;
		}  else if (lett == ' ') {
			x += size * 7;
			continue;
		} 
		p = x;
		for (j = 0; j < 5; j++) {
			for (k = 0; k < 10; k++) {
				if (lettgra[lett - 'A'][j][k]) {
					for (m = 0; m < size; m++)
						for (n = 0; n < size; n++)
							fb_draw_point(x+m, y+n, 0xFE);
				}
				x += size;
			}
			x = p; 
			y += size;
		}
		y = p2;
		x += size*7;	
	}
}
